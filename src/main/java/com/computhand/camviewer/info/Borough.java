package com.computhand.camviewer.info;

import java.util.List;

public class Borough {
	
	private List<float[]> coordinates;
	private String nom;
	private String type;
	private String codeId;
	private String abrev;
	private long num;
	private String codeMamrot;
	private double aire;
	private long munId;
	private double perim;
	
	/**
	 * @return the coordinates
	 */
	public List<float[]> getCoordinates() {
		return coordinates;
	}
	/**
	 * @param coordinates the coordinates to set
	 */
	public void setCoordinates(List<float[]> coordinates) {
		this.coordinates = coordinates;
	}
	/**
	 * @return the nom
	 */
	public String getNom() {
		return nom;
	}
	/**
	 * @param nom the nom to set
	 */
	public void setNom(String nom) {
		this.nom = nom;
	}
	/**
	 * @return the type
	 */
	public String getType() {
		return type;
	}
	/**
	 * @param type the type to set
	 */
	public void setType(String type) {
		this.type = type;
	}
	/**
	 * @return the codeId
	 */
	public String getCodeId() {
		return codeId;
	}
	/**
	 * @param codeId the codeId to set
	 */
	public void setCodeId(String codeId) {
		this.codeId = codeId;
	}
	/**
	 * @return the abrev
	 */
	public String getAbrev() {
		return abrev;
	}
	/**
	 * @param abrev the abrev to set
	 */
	public void setAbrev(String abrev) {
		this.abrev = abrev;
	}
	/**
	 * @return the num
	 */
	public long getNum() {
		return num;
	}
	/**
	 * @param num the num to set
	 */
	public void setNum(long num) {
		this.num = num;
	}
	/**
	 * @return the codeMamrot
	 */
	public String getCodeMamrot() {
		return codeMamrot;
	}
	/**
	 * @param codeMamrot the codeMamrot to set
	 */
	public void setCodeMamrot(String codeMamrot) {
		this.codeMamrot = codeMamrot;
	}
	/**
	 * @return the aire
	 */
	public double getAire() {
		return aire;
	}
	/**
	 * @param aire the aire to set
	 */
	public void setAire(double aire) {
		this.aire = aire;
	}
	/**
	 * @return the munId
	 */
	public long getMunId() {
		return munId;
	}
	/**
	 * @param munId the munId to set
	 */
	public void setMunId(long munId) {
		this.munId = munId;
	}
	/**
	 * @return the perim
	 */
	public double getPerim() {
		return perim;
	}
	/**
	 * @param perim the perim to set
	 */
	public void setPerim(double perim) {
		this.perim = perim;
	}
	
	
	
}
